export const learningCards: Card[] = learningTracks.flatMap((track) =>
  (lessonsByTrack[track.id] || []).map((lesson, index) => makeLearningCard(track, index, lesson))
);

export function getLearningTrackById(trackId: string): LearningTrack | undefined {
  return learningTracks.find((track) => track.id === trackId);
}

export function getLearningCardsByTrack(trackId: string): Card[] {
  const track = getLearningTrackById(trackId);
  if (!track) return [];
  return learningCards.filter((card) => card.book === track.name);
}
