function makeChapter(insight: string, bullets: [string, string, string], action: string): Chapter {
  return {
    title: "Lesson Notes",
    readingTimeMinutes: 3,
    content: [
      insight,
      "",
      "## What matters",
      `- ${bullets[0]}`,
      `- ${bullets[1]}`,
      `- ${bullets[2]}`,
      "",
      "## Try this",
      action,
    ].join("\n"),
  };
}

function makeLearningCard(track: LearningTrack, index: number, lesson: LessonSeed): Card {
  return {
    id: `learn-${track.id}-${index + 1}`,
    author: "BloomScroll Learning",
    book: track.name,
    topic: lesson.topics,
    genre: "self-help",
    quote: lesson.hook,
    insight: lesson.insight,
    image_url: "https://images.unsplash.com/photo-1516321318423-f06f85e504b3?w=800",
    read_time_seconds: 40,
    isPremium: true,
    chapter: makeChapter(lesson.insight, lesson.bullets, lesson.action),
  };
}

